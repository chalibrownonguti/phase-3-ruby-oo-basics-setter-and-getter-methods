class Dog
    def initialize(name, breed)
        @name = name
        @breed = breed
      end
      
      # Getter method for @name
      def name
        @name
      end
      
      # Setter method for @name
      def name=(new_name)
        @name = new_name
      end
      
      # Getter method for @breed
      def breed
        @breed
      end
      
      # Setter method for @breed
      def breed=(new_breed)
        @breed = new_breed
      end
end